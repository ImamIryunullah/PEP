<script>
import { onMounted, ref, onUnmounted } from 'vue';
import ThreeDViewer from '@/components/ThreeDViewer.vue';

import NavbarDashboard from '@/components/NavbarDashboard.vue';
import FooterDashboard from '@/components/FooterDashboard.vue';

export default {
  name: "MiniOlympicPage",
  components: {
    NavbarDashboard,
    FooterDashboard,
    ThreeDViewer
  },
  mounted() {
    console.log('Component mounted');

  },
  setup() {
    const horizontalSection = ref(null);
    const currentSlide = ref(0);
    const isHorizontalScrolling = ref(false);
    const showAll = ref(false);
    const slideCount = 6;

    const olahragaList = [
      { nama: 'Badminton', gambar: '/assets/bulutangkis.png', grid: 'col-span-2 row-span-1' },
      { nama: 'Basket', gambar: '/assets/berita/basket.png', grid: 'col-span-1 row-span-1' },
      { nama: 'E-Sport', gambar: '/assets/berita/esport.png', grid: 'col-span-1 row-span-1' },
      { nama: 'Lari', gambar: '/assets/berita/lari.png', grid: 'col-span-2 row-span-2' },
      { nama: 'Senam', gambar: '/assets/berita/senam.png', grid: 'col-span-1 row-span-1' },
      { nama: 'Tenis Lapangan', gambar: '/assets/berita/tenislap.jpg', grid: 'col-span-1 row-span-1' },
    ];

    const handleWheel = (e) => {
      const section = horizontalSection.value;
      const wrapper = document.getElementById('horizontal-wrapper');

      if (!section || !wrapper) return;

      const sectionTop = section.offsetTop;
      const scrollY = window.scrollY;

      const isInSection = scrollY >= sectionTop && scrollY < sectionTop + window.innerHeight;
      if (!isInSection) return;

      e.preventDefault();

      if (isHorizontalScrolling.value) return;

      const direction = e.deltaY > 0 ? 1 : -1;
      const nextSlide = Math.max(0, Math.min(slideCount - 1, currentSlide.value + direction));
      if (nextSlide === currentSlide.value) return;

      isHorizontalScrolling.value = true;
      currentSlide.value = nextSlide;
      wrapper.style.transform = `translateX(-${currentSlide.value * 100}vw)`;

      setTimeout(() => {
        isHorizontalScrolling.value = false;
      }, 500);
    };

    onMounted(() => {
      const section = horizontalSection.value;
      if (section) {
        section.addEventListener('wheel', handleWheel, { passive: false });
      }
    });

    onUnmounted(() => {
      const section = horizontalSection.value;
      if (section) {
        section.removeEventListener('wheel', handleWheel);
      }
    });
    const openingImages = [
      '/opening/kiri.png',
      '/opening/tengahkiri.png',
      '/opening/tengah.png',
      '/opening/tengahkanan.png',
      '/opening/kanan.png',
    ];



    return {
      horizontalSection,
      currentSlide,
      showAll,
      olahragaList,
      openingImages
    };
  }
};
</script>

